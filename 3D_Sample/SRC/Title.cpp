//--------------------------------------------------------------------------//
//	●	初期化		：シーンに移る時一回、変数の初期化 
//
//--------------------------------------------------------------------------//
void	TitleInit(void)
{

	Gs_FontSet(4,32);			// ＤＦＰPOP体　32×32ドット
}

//--------------------------------------------------------------------------//
//	●	内部処理	：シーン中の処理、現在のシーンで行う処理
//
//--------------------------------------------------------------------------//
void	TitleLoop(void)
{

	if (Gs_GetKEYtrg(kKEYSPACE))	SceneChange(GAME_SCENE);
}

//--------------------------------------------------------------------------//
//	●	描画		：シーン中の描画、現在のシーンで表示するBMPはここで
//
//--------------------------------------------------------------------------//
void	TitleDraw(void)
{
	Gs_DrawBox(40,40,WINW-40,WINH-40,ARGB(0xff,0x20,0x20,0x20),ON);
	Gs_DrawText(220,200,"３Ｄ　Ｐｒｏｇｒａｍ",ARGB(0xff,0xf0,0xf0,0xf0));
	Gs_DrawText(240,300,"Push Space",ARGB(0xff,0xf0,0xf0,0xf0));

}

//--------------------------------------------------------------------------//
//	●	ロード		：シーンに入る時、そのシーンで必要なデータの読み込み
//
//--------------------------------------------------------------------------//
short	TitleLoad(void)
{

	return		TITL_SCENE;
}

//--------------------------------------------------------------------------//
//	●	後処理		：現在のシーンから抜ける時、データの破棄
//
//--------------------------------------------------------------------------//
void	TitleExit(void)
{
}

